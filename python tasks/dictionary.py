#dictionary-----------------------------------------------------------------------------------------------
#copy()
dict1 = {'python':1,'java':2,'c++':3}
dict2 = {'html':4,'css':5,'php':6}
dict2 = dict1.copy()
print(dict2)

#update()
dict = {'python':1,'java':2,'c++':3}
dict.update({'html':4})
print(dict)

#values()
dict = {'Apple':'red','Banana':'yellow', 'Grapes':'purple' }
a = dict.values()
print(a)

#keys()
dict = {'Apple':'red','Banana':'yellow', 'Grapes':'purple' }
a = dict.keys()
print(a)

#items()
dict = {'Apple':'red','Banana':'yellow', 'Grapes':'purple' }
a = dict.items()
print(a)

#clear()
dict = {'Apple':'red','Banana':'yellow', 'Grapes':'purple' }
dict.clear()
print(dict)










