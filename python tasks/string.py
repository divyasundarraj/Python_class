
#String------------------------------------------------------------------------------------------------------
#capitalize()
str = 'python programming language'
a = str.capitalize()
print(a)

#count()
str = 'python programming language'
a = str.count("p")
print(a)

#startswith()
str = 'python programming language'
a = str.startswith('python')
print(a)

#endswith()
str = 'python programming language'
a = str.endswith('language')
print(a)

#length()
str = 'programming'
a = len(str)
print(a)

#uppercase()
str = 'python programming language'
a = str.upper()
print(a)

#lowercase()
str = 'PYTHON PROGRAMMING LANGUAGE'
a = str.lower()
print(a)





