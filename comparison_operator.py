'''comparison operator

== (equal)
!= (not equal)
> (greater than)
< (lesser than)
>= (greater than equal)
<= (lesser than equal)
'''
num_1 =int(input("Enter the first number: "))
num_2 =int(input("Enter the second number: "))

#equal 
equal = num_1 == num_2
print("Equal:",equal)

#not equal 
not_equal = num_1 != num_2
print("not_equal:",not_equal)

#greater than 
greater_than = num_1 > num_2
print("greater_than:",greater_than)

#lesser than 
lesser_than = num_1 < num_2
print("lesser_than:",lesser_than)


#greater than equal to
greater_than_equal_to = num_1 >= num_2
print("greater_than_equal_to :",greater_than_equal_to )

#lesser than equal to
lesser_than_equal_to = num_1 <= num_2
print("lesser_than_equal_to :",lesser_than_equal_to )
