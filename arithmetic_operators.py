'''
+ - addition
- - subtraction
* - multiplication
/ - division
% - modulo
** - exponential powers
// - floor division
'''

#addition
num_1 = 10
num_2 = 20
add = num_1 + num_2
print("addition",add)

#subtraction
sub = num_1 - num_2
print("subtraction",sub)

#multiplication
mul = num_1 - num_2
print("multiplication",mul)

#division
div = num_2 / num_1
print("division",mul)

#modulo (It returns remainder)
mod = num_2 % num_1
print("modulo",mod)

#exponentential power (It returns remainder)
expo = num_2 ** 3 #(20**20**20=8000)
print("exponentential",expo)

#floor division
floor_div = num_2 // num_1
print("floor division",floor_div)

